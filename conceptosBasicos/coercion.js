//tipos de coercion

//pasa el numero a cadena de string y lo acopla
console.log(10 + "5");


//da verdadero convierte un number a string y el valor da igual
console.log("10" == 10);


//toma el espacio vacio como cero y devuelve un valor igual.
console.log([] == 0);

// usamos Number()para convertir el string en numero y asi sumar dos numeros.
console.log(Number("10") + 10);

